//if else if statement in dart
void main() {
  var marks = 80;
  if (marks >= 95) {
    print("Excellent");
  } 
  else if (marks >= 75) {
    print("very good");
  } 
  else if (marks >= 65) {
    print("good");
  } 
  else if (marks >= 50) {
    print("pass");
  } 
  else {
   print("fail");

  }
}
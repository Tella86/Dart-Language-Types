//switch is thge short of else
// it uses case and break
void main() {
  int age = 65;
  switch (age) {
    case 1:
      print("the value is 1");
      break;
    case 2:
      print("the value is 2");

      break;
    case 3:
      print("value is 3");
    default:
      print("the age i not in the range given");
  }
}
